package com.hyperGrid.loopsAndConditions;

public class AboutForLoop {

    public static void main(String[] args) {

        int i = 1;
        System.out.println(i++);
        System.out.println(i);

        System.out.println("*************************");

        int j = 1;
        System.out.println(++j);
        System.out.println(j);

        System.out.println("*****************************");

        for(int k=1; k<5; k++){
            System.out.println(k);
        }

        System.out.println("*****************************");

        for(int k=1; k<5; k++){
            System.out.println(k);
        }



    }
}
